    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        EmployeeRole role = new EmployeeRole();
        LocalDate returnDate = LocalDate.now();
        LocalDate purchaseDate = LocalDate.parse(date.getText(), CustomerProduct.getFORMATTER());
        if (role.returnProduct(ssn.getText(), id.getText(), purchaseDate, returnDate) != -1) {
            JOptionPane.showMessageDialog(rootPane, "Return applied Successfully", "success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Return Failed", "Fail", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

